function getPreferredLocale() {
  const params = new URLSearchParams(window.location.search);
  const queryLang = params.get('lang');
  if (queryLang && DIGIT_I18N[queryLang]) return queryLang;
  const saved = localStorage.getItem('digit-lang');
  if (saved && DIGIT_I18N[saved]) return saved;
  return 'en';
}

function setText(key, value) {
  document.querySelectorAll(`[data-i18n="${key}"]`).forEach(node => {
    node.textContent = value;
  });
}

function setHtml(key, value) {
  document.querySelectorAll(`[data-i18n-html="${key}"]`).forEach(node => {
    node.innerHTML = value;
  });
}

function applyLocale(locale) {
  const t = DIGIT_I18N[locale] || DIGIT_I18N.en;
  document.documentElement.lang = t.htmlLang || locale;
  localStorage.setItem('digit-lang', locale);

  document.querySelectorAll('[data-i18n]').forEach(node => {
    const key = node.getAttribute('data-i18n');
    if (t[key] != null) node.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(node => {
    const key = node.getAttribute('data-i18n-html');
    if (t[key] != null) node.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-content]').forEach(node => {
    const key = node.getAttribute('data-i18n-content');
    if (t[key] != null) node.setAttribute('content', t[key]);
  });

  document.querySelectorAll('[data-i18n-title]').forEach(node => {
    const key = node.getAttribute('data-i18n-title');
    if (t[key] != null) document.title = t[key];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(node => {
    const key = node.getAttribute('data-i18n-placeholder');
    if (t[key] != null) node.setAttribute('placeholder', t[key]);
  });


  document.querySelectorAll('a[href]').forEach(node => {
    const href = node.getAttribute('href');
    if (!href || href.startsWith('mailto:') || href.startsWith('http://') || href.startsWith('https://') || href.startsWith('#')) return;
    const url = new URL(href, window.location.href);
    url.searchParams.set('lang', locale);
    node.setAttribute('href', `${url.pathname.split('/').pop()}${url.search}`);
  });

  const current = new URL(window.location.href);
  current.searchParams.set('lang', locale);
  window.history.replaceState({}, '', `${current.pathname}${current.search}`);

  const switcher = document.getElementById('language-switcher');
  if (switcher) switcher.value = locale;
  document.dispatchEvent(new CustomEvent('digit:locale-applied', { detail: { locale } }));
}

document.addEventListener('DOMContentLoaded', () => {
  const switcher = document.getElementById('language-switcher');
  const initial = getPreferredLocale();
  if (switcher) {
    switcher.addEventListener('change', e => applyLocale(e.target.value));
  }
  applyLocale(initial);
});
